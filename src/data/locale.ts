const TABLES: Record<string, Record<string, NsMap>> = { ru: RU };

// Return a copy of `def` with the active locale's translated string fields merged
// in. English (and any untranslated entry) passes through unchanged.
export function localizeDef<T extends { id: string }>(ns: string, def: T): T {
  const table = TABLES[getLocale()];
  const tr = table?.[ns]?.[def.id];
  return tr ? ({ ...def, ...tr } as T) : def;
}

// Story nodes need a bespoke localizer because choices are an array. Translations
// are keyed by node id; `choices` maps positionally onto node.choices (flag
// fields and targets are preserved — only display text changes). Untranslated
// nodes/choices fall through to English. Display-only: the game layer keeps
// using the original node for navigation, so indexes stay aligned.
export interface StoryTr { title?: string; text?: string; choices?: (string | null)[] }
const STORY: Record<string, Record<string, StoryTr>> = { ru: STORY_RU() };

export function localizeStoryNode(node: StoryNode): StoryNode {
  const tr = STORY[getLocale()]?.[node.id];
  if (!tr) return node;
  return {
    ...node,
    title: tr.title ?? node.title,
    text: tr.text ?? node.text,
    choices: tr.choices
      ? node.choices.map((c, i) => ({ ...c, text: tr.choices![i] ?? c.text }))
      : node.choices,
  };
}

